// rule for naming variable 

// you can not start with number
// example :-
// 1value (invalid)
// value1 (valid)
 var value1 = 10;
 console.log(value1);
 // power
 console.log(value1 ** 2);
// add
console.log(value1 + 23);

// you can use only underscore _ or dollar symbol $
// first_name (valid)
// _firstname (valid)
var first_name = 30; // snake case writing
console.log(first_name);

var _firstname = " Raushan"
console.log(_firstname);
// first$name (valid)
// $firstname (valid)
var first$name = 990;
console.log(first$name);

var $firstname = "Golu";
console.log($firstname);

